@router.post("/me/restore-collection")
async def user_restore_lmod_collection(
    body: dict,
    current_user: TokenUser = Depends(get_current_user),
):
    """Restore a saved Lmod collection for the current user by injecting it into .bashrc."""
    collection_name = body.get("collection_name", "")
    if not collection_name:
        raise HTTPException(status_code=400, detail="collection_name is required")

    block = f"""{SENTINEL_START}
# Source Spack/Lmod setup for non-interactive shells (like Slurm jobs)
if [ -f /etc/profile.d/spack_setup.sh ]; then
    source /etc/profile.d/spack_setup.sh
fi
module restore {collection_name}
{SENTINEL_END}"""

    try:
        await _inject_bashrc(current_user.username, block)
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))

    return {"message": f"Collection '{collection_name}' set as default login environment."}
