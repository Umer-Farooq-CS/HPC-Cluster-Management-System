async def _ssh_run(commands: list) -> list:
    executor = SSHExecutor(
        host=settings.MASTER_IP,
        username=settings.MASTER_USER,
        password=settings.MASTER_PASS,
    )
    results = []
    for cmd in commands:
        out = []
        async for line in executor.run_command_stream(cmd):
            out.append(line)
        results.append("\n".join(out))
    return results


async def _write_lua_file(stack: EnvStack):
    """Write the Lua metamodule file to the master node's custom_modules dir."""
    lua_content = _build_lua_content(stack)
    # Escape single-quotes for bash heredoc safety
    escaped = lua_content.replace("'", "'\\''")
    cmd = (
        "mkdir -p /export/apps/custom_modules && "
        f"cat > /export/apps/custom_modules/{stack.name}.lua << 'LUAEOF'\n{lua_content}\nLUAEOF"
    )
    # Also ensure the custom_modules dir is in MODULEPATH via spack_setup.sh (idempotent)
    ensure_cmd = (
        "grep -qxF 'module use /export/apps/custom_modules' /var/lib/warewulf/overlays/nodeconfig/etc/profile.d/spack_setup.sh "
        "|| echo 'module use /export/apps/custom_modules' >> /var/lib/warewulf/overlays/nodeconfig/etc/profile.d/spack_setup.sh; "
        "grep -qxF 'module use /export/apps/custom_modules' /etc/profile.d/spack_setup.sh "
        "|| echo 'module use /export/apps/custom_modules' >> /etc/profile.d/spack_setup.sh"
    )
    await _ssh_run([cmd, ensure_cmd])


async def _delete_lua_file(stack_name: str):
    cmd = f"rm -f /export/apps/custom_modules/{stack_name}.lua"
    await _ssh_run([cmd])


async def _inject_bashrc(username: str, block: str):
    """Remove old managed block from ~/.bashrc and append new one."""
    # Python one-liner to strip old block and append new one
    remove_cmd = (
        f"python3 -c \""
        f"import re, pathlib; "
        f"p = pathlib.Path('/home/{username}/.bashrc'); "
        f"content = p.read_text() if p.exists() else ''; "
        f"cleaned = re.sub(r'\\n?# --- HPC PROFILE MANAGED BLOCK START ---.*?# --- HPC PROFILE MANAGED BLOCK END ---\\n?', '', content, flags=re.DOTALL); "
        f"p.write_text(cleaned)\""
    )
    append_cmd = f"printf '\\n{block}\\n' >> /home/{username}/.bashrc"
    await _ssh_run([remove_cmd, append_cmd])

