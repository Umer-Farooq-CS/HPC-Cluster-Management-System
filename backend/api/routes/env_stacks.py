# Helper: generate Lua metamodule content
# ---------------------------------------------------------------------------

def _build_lua_content(stack: EnvStack) -> str:
    module_list = [m.strip() for m in stack.modules.split(",") if m.strip()]
    load_lines = "\n".join(f'load("{m}")' for m in module_list)
    return f'''-- Auto-generated by HPC Cluster Management System
-- Stack: {stack.display_name} ({stack.name})
-- Category: {stack.category}

help([[{stack.description or stack.display_name}]])
whatis("Version: 1.0")
whatis("Category: {stack.category}")
whatis("Description: {stack.description or stack.display_name}")

-- Load constituent modules
{load_lines}
'''

# ---------------------------------------------------------------------------
# Helper: sentinel-safe bashrc injection
# ---------------------------------------------------------------------------

SENTINEL_START = "# --- HPC PROFILE MANAGED BLOCK START ---"
SENTINEL_END   = "# --- HPC PROFILE MANAGED BLOCK END ---"

def _build_bashrc_block(stack_name: str) -> str:
    """Generates the managed block content for ~/.bashrc"""
    return f"""{SENTINEL_START}
# Source Spack/Lmod setup for non-interactive shells (like Slurm jobs)
if [ -f /etc/profile.d/spack_setup.sh ]; then
    source /etc/profile.d/spack_setup.sh
fi
module purge
module load {stack_name}
{SENTINEL_END}"""

def _build_base_bashrc_block() -> str:
    """Reset block — just loads the base spack module path, no specific stack."""
    return f"""{SENTINEL_START}
# Source Spack/Lmod setup for non-interactive shells (like Slurm jobs)
if [ -f /etc/profile.d/spack_setup.sh ]; then
    source /etc/profile.d/spack_setup.sh
fi
# No environment profile assigned — using base Spack module path
if [ -d /export/apps/spack ]; then
    module use /export/apps/spack/share/spack/lmod/linux-almalinux9-x86_64/Core
fi
{SENTINEL_END}"""

