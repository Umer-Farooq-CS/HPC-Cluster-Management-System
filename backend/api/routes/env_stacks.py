@router.post("/me/select")
async def user_select_profile(
    body: UserEnvProfileUpdate,
    current_user: TokenUser = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
):
    """User selects (or clears) their own environment profile."""
    if body.stack_name == "" or body.stack_name == "none":
        current_user.env_profile = None
        await db.commit()
        try:
            await _inject_bashrc(current_user.username, _build_base_bashrc_block())
        except Exception as e:
            print(f"[WARN] Could not reset bashrc for {current_user.username}: {e}")
        return {"message": "Profile reset to base Spack environment."}

    # Verify stack exists
    result = await db.execute(select(EnvStack).where(EnvStack.name == body.stack_name))
    stack = result.scalars().first()
    if not stack:
        raise HTTPException(status_code=404, detail=f"Stack '{body.stack_name}' not found")

    current_user.env_profile = stack.name
    await db.commit()

    try:
        await _inject_bashrc(current_user.username, _build_bashrc_block(stack.name))
    except Exception as e:
        print(f"[WARN] Could not inject bashrc for {current_user.username}: {e}")

    return {"message": f"Profile switched to '{stack.display_name}'. It will load on your next login."}


@router.post("/me/save-collection")
async def user_save_lmod_collection(
    body: dict,
    current_user: TokenUser = Depends(get_current_user),
):
    """
    Triggers 'module save <collection_name>' on the master node for the current user.
    This lets users save a named Lmod Collection from their current loaded modules.
    """
    collection_name = body.get("collection_name", "my_env")
    if not collection_name or not collection_name.replace("-", "").replace("_", "").isalnum():
        raise HTTPException(status_code=400, detail="Invalid collection name. Use only letters, numbers, hyphens, underscores.")

    # Run as the actual user via sudo -u
    cmd = f"sudo -u {current_user.username} bash -c 'source /etc/profile.d/spack_setup.sh && module save {collection_name}'"
    try:
        await _ssh_run([cmd])
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))

    return {"message": f"Lmod collection '{collection_name}' saved for user '{current_user.username}'."}


