@router.get("/", response_model=List[EnvStackResponse])
async def list_stacks(db: AsyncSession = Depends(get_db)):
    """List all environment stacks. Public — all authenticated users can read."""
    result = await db.execute(select(EnvStack).order_by(EnvStack.id))
    stacks = result.scalars().all()
    return [
        EnvStackResponse(
            id=s.id,
            name=s.name,
            display_name=s.display_name,
            description=s.description,
            category=s.category,
            modules=[m.strip() for m in s.modules.split(",") if m.strip()],
            created_at=s.created_at,
        )
        for s in stacks
    ]


@router.post("/", response_model=EnvStackResponse)
async def create_stack(
    stack_in: EnvStackCreate,
    current_user: TokenUser = Depends(get_admin_user),
    db: AsyncSession = Depends(get_db),
):
    result = await db.execute(select(EnvStack).where(EnvStack.name == stack_in.name))
    if result.scalars().first():
        raise HTTPException(status_code=400, detail=f"Stack '{stack_in.name}' already exists.")

    new_stack = EnvStack(
        name=stack_in.name,
        display_name=stack_in.display_name,
        description=stack_in.description or "",
        category=stack_in.category,
        modules=",".join(stack_in.modules),
    )
    db.add(new_stack)
    await db.commit()
    await db.refresh(new_stack)

    # Write Lua file on master node
    try:
        await _write_lua_file(new_stack)
    except Exception as e:
        # Log but don't fail — file can be retried
        print(f"[WARN] Could not write .lua file for {new_stack.name}: {e}")

    return EnvStackResponse(
        id=new_stack.id,
        name=new_stack.name,
        display_name=new_stack.display_name,
        description=new_stack.description,
        category=new_stack.category,
        modules=stack_in.modules,
        created_at=new_stack.created_at,
    )


@router.put("/{stack_id}", response_model=EnvStackResponse)
async def update_stack(
    stack_id: int,
    stack_in: EnvStackUpdate,
    current_user: TokenUser = Depends(get_admin_user),
    db: AsyncSession = Depends(get_db),
):
    result = await db.execute(select(EnvStack).where(EnvStack.id == stack_id))
    stack = result.scalars().first()
    if not stack:
        raise HTTPException(status_code=404, detail="Stack not found")

    if stack_in.display_name is not None:
        stack.display_name = stack_in.display_name
    if stack_in.description is not None:
        stack.description = stack_in.description
    if stack_in.category is not None:
        stack.category = stack_in.category
    if stack_in.modules is not None:
        stack.modules = ",".join(stack_in.modules)

    await db.commit()
    await db.refresh(stack)

    try:
        await _write_lua_file(stack)
    except Exception as e:
        print(f"[WARN] Could not regenerate .lua file for {stack.name}: {e}")

    return EnvStackResponse(
        id=stack.id,
        name=stack.name,
        display_name=stack.display_name,
        description=stack.description,
        category=stack.category,
        modules=[m.strip() for m in stack.modules.split(",") if m.strip()],
        created_at=stack.created_at,
    )


