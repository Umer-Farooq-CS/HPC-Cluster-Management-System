@router.post("/{stack_id}/assign/{username}")
async def assign_stack_to_user(
    stack_id: int,
    username: str,
    current_user: TokenUser = Depends(get_admin_user),
    db: AsyncSession = Depends(get_db),
):
    """Admin assigns an environment profile to a specific user."""
    result = await db.execute(select(EnvStack).where(EnvStack.id == stack_id))
    stack = result.scalars().first()
    if not stack:
        raise HTTPException(status_code=404, detail="Stack not found")

    user_result = await db.execute(select(User).where(User.username == username))
    user = user_result.scalars().first()
    if not user:
        raise HTTPException(status_code=404, detail="User not found")

    user.env_profile = stack.name
    await db.commit()

    try:
        await _inject_bashrc(username, _build_bashrc_block(stack.name))
    except Exception as e:
        print(f"[WARN] Could not inject bashrc for {username}: {e}")

    return {"message": f"Profile '{stack.display_name}' assigned to '{username}'."}


